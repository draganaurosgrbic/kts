package com.example.demo.dto;

public class DoubleDTO {
	
	private double value;

	public DoubleDTO() {
		super();
	}

	public DoubleDTO(double value) {
		super();
		this.value = value;
	}

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}

}
