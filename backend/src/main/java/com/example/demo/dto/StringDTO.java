package com.example.demo.dto;

public class StringDTO {
	
	private String value;

	public StringDTO() {
		super();
	}

	public StringDTO(String value) {
		super();
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
