package com.example.demo.dto;

public class BooleanDTO {
	
	private boolean value;

	public BooleanDTO() {
		super();
	}

	public BooleanDTO(boolean value) {
		super();
		this.value = value;
	}

	public boolean isValue() {
		return value;
	}

	public void setValue(boolean value) {
		this.value = value;
	}

}
